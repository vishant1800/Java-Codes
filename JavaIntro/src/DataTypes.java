import java.awt.*;

public class DataTypes {
    public static void main(String args[]){

        System.out.println("Datatypes in Java");
        System.out.println("int " + 10);

        System.out.println("float " + 10.2f); //if f not mentioned then it is treated as double by default

        System.out.println("char " + 'A');

        System.out.println("String " + "Vishant");

        System.out.println("double " + 3.1425742123232535325);

        System.out.println("boolean " + true);

        System.out.println(new char[] {'J', 'a', 'v', 'a'});

        System.out.println(Integer.valueOf(10));

        byte a = 14;
        System.out.println("byte " + a);

        long b = 202842098;
        System.out.println("long " + b);

        short num = 12345;
        System.out.println("short " + b);
    }
}
